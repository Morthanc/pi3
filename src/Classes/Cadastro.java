    private void jButtonCadastrarArmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarArmaActionPerformed

        if (jTextFieldNomeArma.toString().equals("")
                || jComboBoxTipoArma.getSelectedItem().toString().equals("<tipo>")
                || ((Number) jSpinnerCalibreArma.getValue()).intValue() == 0
                || ((Number) jSpinnerCapacidade.getValue()).intValue() == 0
                || ((Number) jSpinnerComprimentArma.getValue()).intValue() == 0
                || jTextFieldFabricanteArma.toString().equals("")
                || ((Number) jSpinnerQuantidadeArma.getValue()).intValue() == 0) {

            JOptionPane.showMessageDialog(rootPane, "Algum campo obrigatorio não foi preenchido", "Cadastro", WIDTH);

        } else {
            produto.setNome(jTextFieldNomeArma.getText());
            produto.setTipo(jComboBoxTipoArma.getName());
            produto.setCalibre(jSpinnerCalibreArma.getValue().toString());
            produto.setCapacidade(jSpinnerCapacidade.getValue().toString());
            produto.setComprimentoTotal(jSpinnerComprimentArma.getValue().toString());
            produto.setFabricante(jTextFieldFabricanteArma.getText());
            produto.setQuantidade(produto.getQuantidade() + 1);

            if (produto.ficoTudoBunito() == true) {
                JOptionPane.showMessageDialog(rootPane, "Cadastro realizado com sucesso!", "Cadastro", WIDTH);
                try {
                    relatorio();
                } catch (JAXBException | IOException ex) {
                    Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar!", "cadastro", WIDTH);
            }

        }

        jTextFieldNomeArma.setText("");
        jTextFieldFabricanteArma.setText("");
        jSpinnerCalibreArma.setValue(0);
        jSpinnerCapacidade.setValue(0);
        jSpinnerComprimentArma.setValue(0);
        jComboBoxTipoArma.setSelectedItem("<tipo>");
    }//GEN-LAST:event_jButtonCadastrarArmaActionPerformed


    private void jButtonCadastrarFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarFornecedorActionPerformed
        Endereco endereco = new Endereco("", "", "", "", "", "");

        if (jTextFieldLogradouroFornecedor.toString().equals("")
                || jTextFieldCepFornecedor.toString().equals("")
                || jTextFieldNumeroFornecedor.toString().equals("")
                || jTextFieldCidadeFornecedor.toString().equals("")
                || jComboBoxEstadoFornecedor.getSelectedItem().toString().equals("xx")
                || jTextFieldNomeFantasiaFornecedor.toString().equals("")
                || jTextFieldRazaoSocialFornecedor.toString().equals("")
                || jTextFieldCnpjFornecedor.toString().equals("")
                || jTextFieldeMailFornecedor.toString().equals("")) {

            JOptionPane.showMessageDialog(rootPane, "Algum campo não foi preenchido", "Atencao!!!", WIDTH);

        } else {

            endereco.setLogradouro(jTextFieldLogradouroFornecedor.getText());
            endereco.setComplemento(jTextFieldBairroFornecedor.getText());
            endereco.setNumero(jTextFieldNumeroFornecedor.getText());
            endereco.setCep(jTextFieldCepFornecedor.getText());
            endereco.setCidade(jTextFieldCidadeFornecedor.getText());
            endereco.setEstado(jComboBoxEstadoFornecedor.getSelectedItem().toString());

            if (endereco.fivouBonito() == true) {
                JOptionPane.showMessageDialog(rootPane, "Cadastro Realizado", "Cadastro", WIDTH);

            } else {

                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar", "Cadastro", WIDTH);
            }

        }

        jTextFieldLogradouroFornecedor.setText("");
        jTextFieldBairroFornecedor.setText("");
        jTextFieldCepFornecedor.setText("");
        jTextFieldNumeroFornecedor.setText("");
        jTextFieldCidadeFornecedor.setText("");
        jComboBoxEstadoFornecedor.setSelectedItem("xx");

        jTextFieldNomeFantasiaFornecedor.setText("");
        jTextFieldRazaoSocialFornecedor.setText("");
        jTextFieldCnpjFornecedor.setText("");
        jTextFieldTelefone1Fornecedor.setText("");
        jTextFieldTelefone2Fornecedor.setText("");
        jTextFieldeMailFornecedor.setText("");

    }//GEN-LAST:event_jButtonCadastrarFornecedorActionPerformed

    public void relatorio() throws JAXBException, IOException {

        JAXBContext context = JAXBContext.newInstance(Arma.class);
        Marshaller marshal = context.createMarshaller();

        marshal.marshal(produto, System.out);

        FileWriter writer = new FileWriter("Arma.xml");
        writer.write("Arma.xml");
        marshal.marshal(produto, writer);
    }

